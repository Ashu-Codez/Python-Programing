# backSlash \ escape character
# \n - newline
# \t - tab
# \b - backspace
# \r - carriage return
# \f - form feed
# \v - vertical tab
# \\ - backslash
# \' - single quote
# \" - double quote (escape Sequence)

print("Hello\nWorld")
print("Hello\tWorld")
print("Hello\bWorld")
print("Hello\rWorld")
print("Hello\fWorld")
print("Hello\vWorld")
print("Hello\\World")
print("Hello\'World")
print("Hello\"World")
