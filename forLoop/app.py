# for loop
for i in range(10):  # start from 0 and end at 9
    print(i)

for i in range(1, 10):  # start from 1 and end at 9
    print(i)

for i in range(1, 10, 2):  # start from 1 and end at 9 and increment by 2
    print(i)

for i in range(1, 10, -1):  # start from 1 and end at 9 and decrement by 1
    print(i)

for i in range(1, 10, -2):  # start from 1 and end at 9 and decrement by 2
    print(i)
